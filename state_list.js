window.onload = function () {
	var stateSel = document.getElementById("stateSel"),
		districtSel = document.getElementById("districtSel"),
		tehsilSel = document.getElementById("tehsilSel");
	for (var state in stateObject) {
		stateSel.options[stateSel.options.length] = new Option(state, state);
	}
	stateSel.onchange = function () {
		districtSel.length = 1; // remove all options bar first
		tehsilSel.length = 1; // remove all options bar first
		if (this.selectedIndex < 1) return; // done 
		for (var district in stateObject[this.value]) {
			districtSel.options[districtSel.options.length] = new Option(district, district);
		}
	}
	stateSel.onchange(); // reset in case page is reloaded
	districtSel.onchange = function () {
		tehsilSel.length = 1; // remove all options bar first
		if (this.selectedIndex < 1) return; // done 
		var tehsil = stateObject[stateSel.value][this.value];
		for (var i = 0; i < tehsil.length; i++) {
			tehsilSel.options[tehsilSel.options.length] = new Option(tehsil[i], tehsil[i]);
		}
	}
}
